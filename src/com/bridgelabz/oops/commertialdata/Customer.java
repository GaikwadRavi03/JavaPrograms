package com.bridgelabz.oops.commertialdata;

public class Customer {
	private int name;
	private int Bal;
	private int shares;

	public int getName() {
		return name;
	}

	public void setName(int name) {
		this.name = name;
	}

	public int getBal() {
		return Bal;
	}

	public void setBal(int bal) {
		Bal = bal;
	}

	public int getShares() {
		return shares;
	}

	public void setShares(int shares) {
		this.shares = shares;
	}

}
